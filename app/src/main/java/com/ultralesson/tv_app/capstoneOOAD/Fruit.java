package com.ultralesson.tv_app.capstoneOOAD;

public class Fruit
{
    String color;
    int size;
    String type;
    public Fruit(String color, int size, String type)
    {
        this.color = color;
        this.size = size;
        this.type = type;
    }
    public String getColor()
    {
        return color;
    }
    public int getSize()
    {
        return size;
    }
    public String getType()
    {
        return type;
    }
}
